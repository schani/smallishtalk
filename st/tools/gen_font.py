# Lowercase reuses uppercase (v1).
for c in "abcdefghijklmnopqrstuvwxyz":
    G.setdefault(c, G[c.upper()])


def row_byte(pattern_row):
    b = 0
    for x in range(8):
        if x < len(pattern_row) and pattern_row[x] == "#":
            b |= 1 << (7 - x)
    return b


def glyph_for(code):
    ch = chr(code)
    return G.get(ch, ["........"] * 8)  # unlisted printable -> blank


def build_bytes():
    # Row-major layout of the 8*COUNT wide x 8 tall strike Form.
    # stride = ceil(8*COUNT / 8) = COUNT bytes per row.
    out = []
    for y in range(HEIGHT):
        for g in range(COUNT):
            out.append(row_byte(glyph_for(FIRST + g)[y]))
    return out


def main():
    data = build_bytes()
    assert len(data) == COUNT * HEIGHT, len(data)
    lines = []
    w = "        "  # 8-space indent inside the literal
    body = " ".join(str(b) for b in data)
    # Wrap the ByteArray literal to a reasonable width.
    wrapped = []
    cur = "\t^#["
    for tok in body.split(" "):
        if len(cur) + len(tok) + 1 > 90:
            wrapped.append(cur)
            cur = "\t   " + tok
        else:
            cur += (" " if not cur.endswith("[") else "") + tok
    cur += "]"
    wrapped.append(cur)
    bits_literal = "\n".join(wrapped)

    print('"GENERATED by st/tools/gen_font.py — DO NOT EDIT. The original 8x8')
    print(' fixed strike font (UI.md §5.5), public domain; kept as StrikeFont')
    print(' classic so pixel-golden tests stay font-independent. Bytes are the')
    print(' row-major layout of the 8*95 x 8 glyph strike Form (ASCII 32..126)."')
